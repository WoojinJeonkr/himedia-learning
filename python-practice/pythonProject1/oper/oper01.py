# 연산자(operator)
# 연산을 해주는 것, 간단한 연산을 해주는 기호!!
# +, -, ==(비교), =(대입, 할당)
# 산술연산자(사칙연산) : +, -, *, /(나눈 값의 나머지 o), //(나눈 값의 나머지 x)
#                    %(나머지)

# n1 = 222; n2 = 100
n1 = 222
n2 = 100

print(n1, '과', n2, '의 합은 ', n1 + n2)
print(n1, '과', n2, '의 차는 ', n1 - n2)
print(n1, '과', n2, '의 곱은 ', n1 * n2)
print(n1, '과', n2, '의 나눈 값은 ', n1 / n2)
print(n1, '과', n2, '의 나눈 값은 ', n1 // n2)
print(n1, '과', n2, '의 나눈 값의 나머지는 ', n1 % n2)

